num1 = 10
num2 = 30
# code for addtion
def addtion(num1, num2):
    return num1 + num2


ans = addtion(num1, num2)
print(f"The addition of {num1} and {num2} is :{ans}")
# code for substraction
def substraction(num1, num2):
    return num1 - num2


ans = substraction(num1, num2)
print(f"The substraction of {num1} and {num2} is :{ans}")
# code for multiplication
def multiplication(num1, num2):
    return num1 * num2


ans = multiplication(num1, num2)
print(f"The multiplication of {num1} and {num2} is :{ans}")
# code for division
def division(num1, num2):
    return num1 / num2


ans = division(num1, num2)
print(f"The division of {num1} and {num2} is :{ans}")
# calculate percentage
def percentage(num1):
    return (num1 * 5) / 100


ans = percentage(num1)
print(f"The percentage of {num1} is :{ans}")
